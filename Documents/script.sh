#!/bin/bash

PS3='tentukan pilihanmu : '
echo
menu=("Ganti Wallpaper" "Ganti Icon" "Ganti Tema" "tambah user" "quit")
select opt in "${menu[@]}"

do
	case $opt in
		"${menu[0]}")
		echo "Masukkan path file : "
		read wp
		while [  ! -f $wp ]
			do
				echo "file not found"
				read wp
			done
		gsettings set org.gnome.desktop.background picture-uri file://$wp
		;;

		"${menu[1]}")
		echo "masukkan tema icon yang akan dipilih : "
		read wp
		gsettings set org.gnome.desktop.interface icon-theme "$wp"
		;;

		"${menu[2]}")
		echo "masukkan nama tema : "
		read th
		gsettings set org.gnome.desktop.interface gtk-theme "$th"
		;;

		"${menu[3]}")
		echo "masukkan nama user baru : "
		read nm
		adduser "$nm"
		;;

		"quit")
		break ;;
		*) echo invalid menu ;;
	esac
done













































































































































































































































































































































































































